/**
 * HaneriSilhouette コンポーネントのプロパティ
 */
interface HaneriSilhouetteProps {
  /** 半襟の塗りつぶし色（CSS カラーコード） */
  color: string;
  /** 追加の CSS クラス名 */
  className?: string;
}

/**
 * 半襟のシルエットを SVG で描画するコンポーネント
 * 着物と同じ viewBox 1000×2000 の座標系で、首元の V 字開口部（上端 (350,0)〜(650,0)、
 * 左衿線 (350,0)→(495,200)、右衿線 (650,0)→(500,200)）の内側に左右 2 枚の帯を描く。
 * どちらの帯も、着物の衿が y=0 に達する点（左 (350,0)・右 (650,0)）そのものから開始して
 * 上端の幅を持たず、下側のエッジは衿線に沿う直線、上側のエッジはちょっと上に凸のカーブで
 * 下降して、反対側の衿線に隠れるタイミングで下側のエッジと平行になりきらないぐらいの
 * 角度で終わる。右の帯（前面）は頂点まで開口部を埋め、左の帯（背面）は
 * 右の帯の上側エッジに沿った切断線で先端をカットし、間に背景が覗く描画しない細い線を
 * 残すことで右前を表現する。
 * @param props - コンポーネントのプロパティ
 * @param props.color - 半襟の塗りつぶし色
 * @param props.className - 追加の CSS クラス名
 * @returns 半襟シルエットの SVG 要素
 */
export function HaneriSilhouette({ color, className = "" }: HaneriSilhouetteProps) {
  return (
    <svg viewBox="0 0 1000 2000" className={className} xmlns="http://www.w3.org/2000/svg">
      {/* 左の半襟（画面左・下に重なる側）。下側エッジは左衿線に沿い、先端は右の帯の
          上側エッジに沿った緩いカーブでカットして、右の帯との間に細い隙間を残す */}
      <path
        d={`
          M 0350 0000
          L 0447 0134
          C 0463 0115 0474 0106 0494 0091
          C 0462 0067 0400 0028 0350 0000
          Z
        `}
        fill={color}
      />
      {/* 右の半襟（画面右・前面に重なる側）。下側エッジは右衿線に沿って頂点まで下り、
          上側エッジは上に凸のカーブで左衿線上の (452,140) に達して衿線の裏に隠れる */}
      <path
        d={`
          M 0650 0000
          L 0500 0200
          L 0495 0200
          L 0452 0140
          C 0488 0095 0590 0033 0650 0000
          Z
        `}
        fill={color}
      />
    </svg>
  );
}
